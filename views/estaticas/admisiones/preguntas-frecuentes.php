                        <summary>¿Puedo iniciar si aún estoy comparando escuelas?</summary>
                        <div class="faq-ans">Sí. De hecho, iniciar a tiempo reduce la presión y mantiene tus opciones abiertas mientras tomas la decisión.</div>
